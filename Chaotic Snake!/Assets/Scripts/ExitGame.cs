using UnityEngine;
#if UNITY_EDITOR
    using UnityEditor;
#endif

public class ExitGame : MonoBehaviour
{
    public static ExitGame Instance;

    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(this);
        }
        else
        {
            Instance = this;
        }
    }

    public void Quit()
    {
#if UNITY_EDITOR
        EditorApplication.isPlaying = false;
#else
        Application.Quit();
#endif
    }
}
